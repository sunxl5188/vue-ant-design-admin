        把应对突发事件作为组织力建设重大课题，具有现实紧迫性。新形势新任务条件下，各种矛盾和问题集中地叠加式地出现，各种挑战和考验异常严峻。从大的国际环境看，我们正处于“百年未有之大变局”中，斗争的现实一点也不省心；从具体的国内局势看，各种艰苦卓绝的攻坚战接踵而至，改革、发展和稳定的任务一点也不轻松，中华民族伟大复兴的中国梦的确不是敲锣打鼓就能实现的。在这种严峻形势下，对有可能随时出现的突发事件和各种形态的危机得有充分准备，注重风险防范化解，从“最坏处”准备、向“最好处”努力。这次疫情防控是一次难得的组织力“练兵”，实践证明是合格的，交了一份党和人民满意的答卷，然而不能满足、不可骄傲，更不应懈怠。组织力建设有许多事要做，包括自身体系的健全完善等，但通过这次疫情防控得到的一个重要启示和认知是，把应对突发事件作为组织力建设的重大课题，具有现实紧迫性。</p>
