        强化组织力在应对突发事件中的作用。规律告诉我们，应对突发事件切忌慌乱无章、杂乱无序、忙乱无效，讲究有力有序有效地展开应对。实现这“三有”，离不开组织力的强大作用。作为组织力重要内容的政治领导力、思想引领力、群众组织力、社会号召力，如何保证并实现？应当具体体现在以下四个方面作用上：一是组织发动作用。突发事件发生了，迅速把各方面力量集中、发动并组织起来，包括思想上的发动、人员上的组织，这是基础环节。二是统筹协调作用。“统”得起、“分”得下，让方方面面的资源最优化、最大化，并且集中到一个目标上来、统一到问题解决上来。三是宣传教育作用。针对突发事件发生后民众心理变化，跟踪社会舆情动态，及时把政策宣传和思想教育贯穿于应对突发事件的全过程，以保持社会稳定和人心安定。四是战斗堡垒和先锋模范作用。9000多万名党员和460多万个基层党组织是世界上任何一个政党都无法比拟的优势，他们分布在各行各业、各个领域和各个层级，把他们的先进性发挥出来，任何矛盾和问题都可以解决。</p>
