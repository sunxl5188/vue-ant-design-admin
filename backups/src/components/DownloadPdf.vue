<script>
import * as _ from 'lodash'
import { getUrlBase64 } from '@~/js/utils'
import html2Canvas from 'html2canvas'
import JsPDF from 'jspdf'

export default {
  name: 'DownloadPdf',
  data () {
    return {
      offsetLeft: 0,
      offsetTop: 0,
      imgData: []
    }
  },
  mounted () {
    let self = this
    self.$nextTick(() => {
    })
  },
  methods: {
    downImg (id) {
      let self = this
      let imgUrl = []
      let maxWidth = 820
      let maxHeight = 1160
      let imgArr = document.all(id).getElementsByTagName('img')
      const $this = $('.pdf-content')
      let contentHeight = $this.outerHeight(true)
      let pageSize = Math.ceil(contentHeight / 1160)
      pageSize = Math.ceil((contentHeight + 100 * pageSize) / 1160)
      self.offsetTop = $this.offset().top
      self.offsetLeft = $this.offset().left
      $this.css({
        width: maxWidth * pageSize,
        height: maxHeight,
        'column-count': pageSize
      })
      // 图片转base64
      for (let i = 0; i < imgArr.length; i++) {
        imgUrl.push(imgArr[i].getAttribute('src'))
        getUrlBase64(imgArr[i].getAttribute('src'), '', '', function (base) {
          imgArr[i].setAttribute('src', base)
        })
      }
      const scale = 2
      // 开始转换图片
      html2Canvas(document.querySelector('#' + id), {
        allowTaint: true,
        scale: scale,
        taintTest: false,
        dpi: window.devicePixelRatio,
        x: self.offsetLeft,
        y: self.offsetTop,
        width: maxWidth * pageSize,
        height: maxHeight
      }).then(function (canvas) {
        let contentWidth = canvas.width
        let contentHeight = canvas.height
        let position = 50
        // a4纸的尺寸[595.28,841.89]，html页面生成的canvas在pdf中图片的宽高
        // 留边50px[515,762]
        let imgWidth = contentWidth * 762 / contentHeight
        let imgHeight = 762
        let leftWidth = contentWidth / scale
        let pageData = canvas.toDataURL('image/jpeg', 1.0)
        let PDF = new JsPDF('', 'pt', 'a4')
        /* let img = new Image()
        img.src = pageData
        document.body.append(img) */
        if (leftWidth <= maxWidth) {
          PDF.addImage(pageData, 'JPEG', position, 40, imgWidth, imgHeight)
        } else {
          while (leftWidth > 0) {
            PDF.addImage(pageData, 'JPEG', position, 40, imgWidth, imgHeight)
            leftWidth -= maxWidth
            position -= 515 + 40
            if (leftWidth > 0) {
              PDF.addPage()
            }
          }
        }
        document.querySelector('#' + id).removeAttribute('style')
        PDF.save('1212.pdf')
      })
      // 还原图片地址
      _.delay(() => {
        for (let i = 0; i < imgArr.length; i++) {
          imgArr[i].setAttribute('src', imgUrl[i])
        }
      }, 5000)
    }
  }
}
</script>

