      <p>
        这是由突发事件的特点和属性来决定的。突发事件是指突然发生，造成或者可能造成严重社会危害，需要采取应急处置措施予以应对的自然灾害、事故灾难、公共卫生事件和社会安全事件。突发事件具有以下几个突出特点：一是突发性。突发事件最基本的特征是突如其来、始料不及甚至意想不到，它常常在人们毫无准备的情况下不期而遇，可谓从天而降。俗话说：“天有不测风云，人有旦夕祸福。”说的就是突发性。二是复杂性。突发事件往往是各种矛盾和问题交织并相互作用的结果，但它所呈现出来的形态和方式又是多种多样，而且复杂多变，最基本的形态是自然灾害、事故灾难、公共卫生事件和社会安全事件等四类。三是传导性。在互联网时代，突发事件好比病毒，传染性强、传播力快，有时都不需要发酵便爆炸开来，而且借助互联网呈几何级发展，产生各种各样的连锁反应，出现轰动效应和辐射效应。四是破坏性。突发事件是严重“危险品”，一旦发生并且处置不当不及时，则危害性大、杀伤力强，有时是大面积、大范围和爆炸式的影响。</p>
